    private void jMenuItem8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem8ActionPerformed
         // TODO add your handling code here:
         //copying
         try {
          String SelectedText = jTextArea1.getSelectedText();
          if(SelectedText != null)
                  {
                      Clipboard board = Toolkit.getDefaultToolkit().getSystemClipboard();
                      StringSelection select = new StringSelection(SelectedText);
                      board.setContents(select, null);
                  }
          else{
              JOptionPane.showMessageDialog(null, "There is no selected text in this document");
          }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jMenuItem8ActionPerformed

    private void jMenuItem9ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem9ActionPerformed
        // TODO add your handling code here:
        //Paste method
        Clipboard clipboard = Toolkit.getDefaultToolkit().getSystemClipboard();
        Transferable transfer = clipboard.getContents(null);
        if(transfer != null && transfer.isDataFlavorSupported(DataFlavor.stringFlavor))
        {
            try {
                String text = (String) transfer.getTransferData(DataFlavor.stringFlavor);
                jTextArea1.insert(text, jTextArea1.getCaretPosition());
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_jMenuItem9ActionPerformed

    private void jMenuItem7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem7ActionPerformed
         // TODO add your handling code here:
         //Cut
         try {
            String selectedText = jTextArea1.getSelectedText();
            if(selectedText != null)
            {
                Clipboard cut = Toolkit.getDefaultToolkit().getSystemClipboard();
                StringSelection cutted = new StringSelection(selectedText);
                cut.setContents(cutted, null);
                int startp = jTextArea1.getSelectionStart();
                int endp = jTextArea1.getSelectionEnd();
                if(startp != endp)
                {
                    jTextArea1.replaceRange("", startp, endp);
                }
            }
            else
            {
            JOptionPane.showMessageDialog(null, "There is no selected text in this document");
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jMenuItem7ActionPerformed

