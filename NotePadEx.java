    private void jMenuItem3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem3ActionPerformed
         try {
        fName = "C:\\Users\\Public\\" + JOptionPane.showInputDialog("File Name")+".txt";
        File nFile = new File(fName);
        if(nFile.createNewFile())
        {
            //JOptionPane.showMessageDialog(null,"The file creation was succesful! "+nFile.getName());
            if(nFile.canRead() && nFile.canWrite())
            {
                FileWriter tFile = new FileWriter(fName);
              
                tFile.write(jTextArea1.getText());
                tFile.close();
                JOptionPane.showMessageDialog(null, "The File was created succefully!");
                
            }
        }
        else
        {
            JOptionPane.showMessageDialog(null,"The file creation was unsuccesful!","Error",JOptionPane.ERROR_MESSAGE);
        }
        }
        catch(Exception e){
             JOptionPane.showMessageDialog(null,"The file creation was unsuccesful!","Error",JOptionPane.ERROR_MESSAGE);
        e.printStackTrace();
        }
    }//GEN-LAST:event_jMenuItem3ActionPerformed

    private void jMenuItem17ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem17ActionPerformed
         // TODO add your handling code here:
         JFileChooser object = new JFileChooser();
         int returnValue = object.showOpenDialog(object);
         if(returnValue == JFileChooser.APPROVE_OPTION)
         {
             try {
                 File selectedFile = object.getSelectedFile();
                 FileReader reader = new FileReader(selectedFile);
                 BufferedReader bufferedReader = new BufferedReader(reader);
                 String line;
                 StringBuilder text = new StringBuilder();

                while ((line = bufferedReader.readLine()) != null) {
                    text.append(line).append("\n");
                }
                jTextArea1.setText(text.toString());
                this.setTitle(fName);
             } catch (Exception e) {
             }
         }
    }//GEN-LAST:event_jMenuItem17ActionPerformed

